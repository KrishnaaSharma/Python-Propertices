
#  1.Index Method

tup1 = (1,2,3,4,3,5,3, 'a', 'b','a')

my_tuple1= tup1.index('a') #7
my_tuple2= tup1.index(3) #2

print(my_tuple1)
print(my_tuple2)


#2. Count method


tup2 = (1,2,3,4,3,5,3, 'a', 'b','a')

my_tuple1= tup2.count('a') # 2
my_tuple2= tup2.count(3) # 3

print(my_tuple1)
print(my_tuple2)
